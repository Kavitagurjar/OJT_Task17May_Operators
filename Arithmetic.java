public class Arithmetic {

    public static void main(String[] args) {
        int a = 50;
        int b = 20;
        int c = a + b;
        System.out.println(c);

        int d = a - b;
        System.out.println(d);

        int e = a * b;
        System.out.println(e);

        int f = b / a;
        System.out.println(f);

        int g = b % a;
        System.out.println(g);

        int h = a % b;
        System.out.println(h);
        
    }
}
